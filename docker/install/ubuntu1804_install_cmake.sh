set -e
set -u
set -o pipefail

sudo apt-get install -y wget gpg-agent

# install cmake, ubuntu 18.04 does not support cmake 3.13
touch  /etc/apt/sources.list.d/cmake.list
echo deb https://apt.kitware.com/ubuntu/ bionic main\
	>> /etc/apt/sources.list.d/cmake.list
wget -O - https://apt.kitware.com/keys/kitware-archive-latest.asc 2>/dev/null | gpg --dearmor - | sudo tee /etc/apt/trusted.gpg.d/kitware.gpg >/dev/null
apt-get update && apt install -y cmake>=3.13

cmake -version
